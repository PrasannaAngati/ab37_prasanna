package whileloop;

public class Whileloop {

	public static void main(String[] args) {
     int age=18;//Initialisation and declaration
     while(age>=10) //condition
     {
    	 int Current_age=1+age;
    	 System.out.println(Current_age);
    	 age--;//Increment or decrement
    	 
    	 
     }
     
	}

}
